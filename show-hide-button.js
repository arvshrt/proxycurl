function toggleHeight1(button) {
    const preContainer = button.previousElementSibling;
    if (preContainer.style.maxHeight === 'none') {
      preContainer.style.maxHeight = '176px';
      button.textContent = 'Show More';
    } else {
      preContainer.style.maxHeight = 'none';
      button.textContent = 'Show Less';
    }
  }

